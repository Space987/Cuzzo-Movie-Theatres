class Category {
    /**
     *
     * @param {*} categoryId Id used to find category.
     * @param {*}  Id used to find category.
     * @param {*} categoryDesc Id used to find category.
     */

    constructor(categoryId, categoryName, categoryDesc) {
        this.categoryId = categoryId;
        this.categoryName = categoryName;
        this.categoryDesc = categoryDesc;
    }
}